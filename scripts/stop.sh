#!/bin/bash
set -e

# =============================================================================
# Stop Script for API Nest
# =============================================================================
# Usage: ./scripts/stop.sh [environment]
# =============================================================================

ENVIRONMENT=${1:-production}

echo "============================================"
echo "  Stopping API Nest - $ENVIRONMENT"
echo "============================================"

docker compose -f docker-compose.$ENVIRONMENT.yml down

echo "============================================"
echo "  Services Stopped"
echo "============================================"
